package day1;

public class Teoria {
	
	public static void main(String[] args) {

		//SINTASSI VARIABILI
		// tipologiaVariabile nomeVariabile = valoreVariabile;
		
		int x = 10;
		System.out.println(x);
		
		//tipi di dati numerici interi
		byte v1 = 100;
		short v2 = 15_000;
		long v3 = 100000000000000L;
		
		//caratteri
		char v4 = 'a';
		char v5 = 82;
		System.out.println(v4);
		System.out.println(v5);
		
		//numeri a virgola mobile
		float v6 = 10.69f;
		double v7 = 10.5678912d;
		
		System.out.println(v6);
		System.out.println(v7);
	}

}
